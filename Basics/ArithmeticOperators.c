#include<stdio.h>
int main(){

    int a = 10;
    

    int b = 3;
    
    int add;
    add = a + b;
    printf("their sum is : %d\n",add);

    int sub;
    sub = a - b;
    printf("their sub is : %d\n",sub);

    int mul;
    mul = a*b;
    printf("their mul is : %d\n",mul);

    int div;
    div = a/b;
    printf("their div is : %d\n",div);

    float mod;
    mod = a % b;
    printf("their mod is : %f\n",mod);

    return 0;
}